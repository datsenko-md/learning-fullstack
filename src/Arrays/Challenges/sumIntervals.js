const sumIntervals = (intervals) => {
  const values = [];
  for (const [begin, end] of intervals) {
    for (let i = begin; i < end; i += 1) {
      if (!values.includes(i)) {
        values.push(i);
      }
    }
  }

  return values.length;
};

export default sumIntervals;
